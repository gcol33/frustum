    use super::*;

    #[test]
    fn test_sphere_isosurface() {
        // Create a 3D volume with a sphere SDF
        let size = 20;
        let mut values = Vec::with_capacity(size * size * size);

        for z in 0..size {
            for y in 0..size {
                for x in 0..size {
                    let fx = (x as f32 / (size - 1) as f32) * 2.0 - 1.0;
                    let fy = (y as f32 / (size - 1) as f32) * 2.0 - 1.0;
                    let fz = (z as f32 / (size - 1) as f32) * 2.0 - 1.0;
                    // Signed distance to sphere of radius 0.7
                    let dist = (fx * fx + fy * fy + fz * fz).sqrt() - 0.7;
                    values.push(dist);
                }
            }
        }

        let volume = Volume::new(
            values,
            [size, size, size],
            [2.0 / (size - 1) as f32; 3],
            [-1.0, -1.0, -1.0],
        );

        let mesh = marching_cubes(&volume, 0.0);

        // Should produce a closed mesh with triangles
        assert!(!mesh.positions.is_empty());
        assert!(!mesh.indices.is_empty());
        assert!(mesh.normals.is_some());

        // Triangle count should be reasonable for a sphere
        let tri_count = mesh.indices.len() / 3;
        assert!(tri_count > 100, "Expected more triangles for sphere, got {}", tri_count);
    }

